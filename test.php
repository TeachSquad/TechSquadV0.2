   	  	 <h1>Join the DONATEinKIND<br/>Movement</h1>
   	  	 <p><span style="font-size:14px">Donating in kind provides an immediate benefit since the receiver can make use of the donated item within the shortest possible time.</span></p>

<p><span style="font-size:14px">Leftover food originating from parties, weddings and restaurants will just go waste, unless it is donated in order to satiate the hunger of deprived people living in slums and on the street. Community fridges and regular food tiffins are other means to feed the poor and hungry. </span></p>

<p><span style="font-size:14px">Old and/or used clothes, shoes, books, textbooks, toys, computers and bicycles that are not being utilized any longer are of no utility to the possessor. If donated, these items would be useful to underprivileged persons from weaker sections of society.</span></p>

<p><span style="font-size:14px">Other items like sanitary pads, blankets, stationary and household goods need to be donated in new condition only. However, there is no bar on donation of new items even in categories&nbsp; where old, used goods are accepted. </span></p>

<p><span style="font-size:14px">DONATEinKIND is here to help you become a donor of food as well as used and new products. If you desire, we co-ordinate between you and donation-seekers so that the process is concluded soonest. Of course, you can proceed on your own by directly contacting organizations and individuals listed herein.</span></p>

<p><span style="font-size:14px">We also urge budding social entrepreneurs to promote donations in kind in their cities and towns, followed by collection and distribution of used and new products. Likewise, we welcome seekers of donations in kind to inform us about their requirements, so that this platform can bring their needs to the attention of our audience of do-gooders.</span></p>

<p><span style="font-size:14px">Let us all take this movement forward to fulfill, in a small way, the mission of ushering in a kinder, caring and supportive world!</span></p>   	  </div>
