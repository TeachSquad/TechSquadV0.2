      <h1>About Us</h1>
      <p><span style="font-size:14px">Donateinkind is a one-stop resource for donations in kind. Through this online platform we seek to popularise the concept of donating in kind. While monetary donations can be made by people with financial means, donating in kind can be done by practically everyone because item being donated is already in use with us.</span></p>

<p><span style="font-size:14px">Hereby we have started-off by providing information on donation seekers so that donors can straightaway contact the needy. However, in due course, we wish to move upwards on the value chain by organizing our own collection centres in major Indian cities, followed by distribution to deserving establishments.</span></p>

<p><span style="font-size:14px">While aspects covered above relate to physical goods, we decided to extend the reach to include human-origin components like blood, hair, mother&#39;s milk and body organs. By all means these too are donations in kind because through these donations kindness is shown to a cancer patient or a pre-term baby or an accident victim.</span></p>

<p><span style="font-size:14px">With this maiden venture of social entrepreneurship, our Advisors and Founder hope to give a filip to donating in kind in India. Shortly, we will launch another public-service venture www.cancerassist.in which will pertain to assistance for cancer patients in India.</span></p>
